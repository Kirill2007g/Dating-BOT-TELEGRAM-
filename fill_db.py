def fill(count: int = 100):
    cursor.execute("SELECT telegram_id FROM profiles")
    existing = {row[0] for row in cursor.fetchall()}

    added = 0
    fake_id = 2_000_000_000

    while added < count:
        if fake_id in existing:
            fake_id += 1
            continue

        gender = random.choice(["мужской", "женский"])
        if gender == "мужской":
            name = random.choice(MALE_NAMES)
            desc = random.choice(MALE_DESC)
            looking_for = random.choice(["женский", "женский", "все"])
        else:
            name = random.choice(FEMALE_NAMES)
            desc = random.choice(FEMALE_DESC)
            looking_for = random.choice(["мужской", "мужской", "все"])

        age  = random.randint(16, 20)
        city = random.choice(CITIES)
        lang = random.choice(["ru", "uk"])

        cursor.execute(
            """INSERT INTO profiles
            (telegram_id, username, name, age, city, gender, looking_for, description, active, language)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, 1, ?)""",
            (fake_id, f"test_{fake_id}", name, age, city, gender, looking_for, desc, lang),
        )
        existing.add(fake_id)
        fake_id += 1
        added += 1

    conn.commit()
    print(f"✅ Добавлено {added} тестовых анкет")
    print(f"   Всего в БД: {len(existing)} профилей")


if __name__ == "__main__":
    fill()
